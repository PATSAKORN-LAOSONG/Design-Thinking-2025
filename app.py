def parse_range(value):
    if pd.isna(value):
        return (None, None)
    
    value = str(value).strip()
    
    if ">=" in value:
        num = int(value.replace(">=", "").strip())
        return (num, float("inf"))
    
    if "-" in value:
        low, high = value.split("-")
        return (int(low.strip()), int(high.strip()))
    
    if "<" in value:
        num = int(value.replace("<", "").strip())
        return (0, num - 1)
    
    return (None, None)

